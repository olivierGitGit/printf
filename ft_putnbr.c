#include "printf.h"

int	ft_putnbr(int x)
{
	int	i;
	int	count;

	count = 1;
	if (x <= -10 || x >= 10)
		count = 1 + ft_putnbr(x / 10);
	if (x >= -9 && x < 0)
	{
		count++;
		write(1, "-", 1);
	}
	if (x % 10 < 0)
		i = -(x % 10) + 48;
	else
		i = (x % 10) + 48;
	write(1, &i, 1);
	return (count);
}
